/* EL1 exception vectors — IRQ + SVC-aware sync */
.section .text
.align 11
.global exception_vectors
.extern aarch64_irq_handler
.extern aarch64_sync_entry

.macro SAVE_ALL
    sub sp, sp, #288
    stp x0, x1, [sp, #0]
    stp x2, x3, [sp, #16]
    stp x4, x5, [sp, #32]
    stp x6, x7, [sp, #48]
    stp x8, x9, [sp, #64]
    stp x10, x11, [sp, #80]
    stp x12, x13, [sp, #96]
    stp x14, x15, [sp, #112]
    stp x16, x17, [sp, #128]
    stp x18, x19, [sp, #144]
    stp x20, x21, [sp, #160]
    stp x22, x23, [sp, #176]
    stp x24, x25, [sp, #192]
    stp x26, x27, [sp, #208]
    stp x28, x29, [sp, #224]
    str x30, [sp, #240]
    mrs x0, sp_el0
    str x0, [sp, #248]
    mrs x0, elr_el1
    str x0, [sp, #256]
    mrs x0, spsr_el1
    str x0, [sp, #264]
    mrs x0, esr_el1
    str x0, [sp, #272]
    mov x0, sp
.endm

.macro RESTORE_ALL
    ldr x0, [sp, #256]
    msr elr_el1, x0
    ldr x0, [sp, #264]
    msr spsr_el1, x0
    ldr x30, [sp, #240]
    ldp x28, x29, [sp, #224]
    ldp x26, x27, [sp, #208]
    ldp x24, x25, [sp, #192]
    ldp x22, x23, [sp, #176]
    ldp x20, x21, [sp, #160]
    ldp x18, x19, [sp, #144]
    ldp x16, x17, [sp, #128]
    ldp x14, x15, [sp, #112]
    ldp x12, x13, [sp, #96]
    ldp x10, x11, [sp, #80]
    ldp x8, x9, [sp, #64]
    ldp x6, x7, [sp, #48]
    ldp x4, x5, [sp, #32]
    ldp x2, x3, [sp, #16]
    ldp x0, x1, [sp, #0]
    add sp, sp, #288
.endm

.macro SYNC_VECTOR
    .align 7
    SAVE_ALL
    bl aarch64_sync_entry
    RESTORE_ALL
    eret
.endm

.macro IRQ_VECTOR
    .align 7
    SAVE_ALL
    bl aarch64_irq_handler
    RESTORE_ALL
    eret
.endm

.macro FAULT_VECTOR
    .align 7
    SAVE_ALL
    bl aarch64_sync_entry
    RESTORE_ALL
    eret
.endm

exception_vectors:
    SYNC_VECTOR
    IRQ_VECTOR
    FAULT_VECTOR
    FAULT_VECTOR
    SYNC_VECTOR
    IRQ_VECTOR
    FAULT_VECTOR
    FAULT_VECTOR
    SYNC_VECTOR
    IRQ_VECTOR
    FAULT_VECTOR
    FAULT_VECTOR
    SYNC_VECTOR
    IRQ_VECTOR
    FAULT_VECTOR
    FAULT_VECTOR
