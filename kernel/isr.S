/* Interrupt stubs — save GPRs, call C handler, iretq */
.macro ISR_NOERR n
.global isr\n
isr\n:
    pushq $0
    pushq $\n
    jmp isr_common
.endm

.macro ISR_ERR n
.global isr\n
isr\n:
    pushq $\n
    jmp isr_common
.endm

.macro IRQ n, i
.global irq\n
irq\n:
    pushq $0
    pushq $\i
    jmp isr_common
.endm

